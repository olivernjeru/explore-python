# While loops are similar
n = 0
while n < 5:
    print(n)
    n +=1

# For Loops are pretty similar as well
# Looping from i = 0 to i = 4
for i in range(5):
    print(i)

# Looping from i = 2 to i = 5
for i in range(2, 6):
    print(i)

# Looping from i = 5 to i = 2
# We pass the negative one to show that we are decrementing by one each time the loop runs
for i in range(5, 2, -1):
    print(i)